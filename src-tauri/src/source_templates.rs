/// Get all builtin templates
pub fn builtin_templates() -> Vec<SourceTemplate> {
    vec![
        template_counter(),
        template_fifo(),
        template_fsm(),
        template_uart_tx(),
        template_spi_master(),
        template_testbench(),
    ]
}

/// Instantiate a template with parameter substitution
pub fn instantiate_template(
    template: &SourceTemplate,
    params: &std::collections::HashMap<String, String>,
) -> BackendResult<String> {
    let mut code = template.source_code.clone();

    for (key, value) in params {
        let pattern = format!("${{{}}}", key);
        code = code.replace(&pattern, value);
    }

    Ok(code)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_template_counter() {
        let template = template_counter();
        assert_eq!(template.id, "counter");
        assert_eq!(template.language, "verilog");
        assert!(!template.source_code.is_empty());
    }

    #[test]
    fn test_template_fifo() {
        let template = template_fifo();
        assert_eq!(template.id, "fifo");
        assert_eq!(template.parameters.len(), 2);
        assert!(template.source_code.contains("empty"));
        assert!(template.source_code.contains("full"));
    }

    #[test]
    fn test_builtin_templates() {
        let templates = builtin_templates();
        assert_eq!(templates.len(), 6);
        assert!(templates.iter().any(|t| t.id == "counter"));
        assert!(templates.iter().any(|t| t.id == "fifo"));
        assert!(templates.iter().any(|t| t.id == "uart_tx"));
    }

    #[test]
    fn test_instantiate_template() {
        let template = template_counter();
        let mut params = std::collections::HashMap::new();
        params.insert("WIDTH".to_string(), "16".to_string());

        let code = instantiate_template(&template, &params).unwrap();
        // Template doesn't use parameter substitution syntax, so verify it returns code
        assert!(!code.is_empty());
    }

    #[test]
    fn test_template_spi_master() {
        let template = template_spi_master();
        assert_eq!(template.id, "spi_master");
        assert_eq!(template.parameters.len(), 2);
        assert!(template.source_code.contains("sclk"));
        assert!(template.source_code.contains("mosi"));
    }

    #[test]
    fn test_template_testbench() {
        let template = template_testbench();
        assert_eq!(template.id, "testbench");
        assert!(template.source_code.contains("tb_top"));
        assert!(template.source_code.contains("timescale"));
    }
}
