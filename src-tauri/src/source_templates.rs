/// Generate FIFO template
pub fn template_fifo() -> SourceTemplate {
    SourceTemplate {
        id: "fifo".to_string(),
        name: "FIFO".to_string(),
        description: "Synchronous FIFO with configurable depth".to_string(),
        language: "verilog".to_string(),
        category: "Memory".to_string(),
        source_code: r#"// Synchronous FIFO
// Generated by CovertEDA

module fifo #(
    parameter WIDTH = 8,
    parameter DEPTH = 256
) (
    input wire clk,
    input wire rst_n,
    input wire write_en,
    input wire read_en,
    input wire [WIDTH-1:0] data_in,
    output reg [WIDTH-1:0] data_out,
    output wire full,
    output wire empty
);

    localparam ADDR_WIDTH = $clog2(DEPTH);
    reg [ADDR_WIDTH-1:0] write_ptr, read_ptr;
    reg [WIDTH-1:0] mem [0:DEPTH-1];

    assign empty = (write_ptr == read_ptr);
    assign full = ((write_ptr[ADDR_WIDTH-1] != read_ptr[ADDR_WIDTH-1]) &&
                   (write_ptr[ADDR_WIDTH-2:0] == read_ptr[ADDR_WIDTH-2:0]));

    always @(posedge clk) begin
        if (!rst_n) begin
            write_ptr <= 0;
            read_ptr <= 0;
        end else begin
            if (write_en && !full) begin
                mem[write_ptr[ADDR_WIDTH-1:0]] <= data_in;
                write_ptr <= write_ptr + 1'b1;
            end
            if (read_en && !empty) begin
                data_out <= mem[read_ptr[ADDR_WIDTH-1:0]];
                read_ptr <= read_ptr + 1'b1;
            end
        end
    end

endmodule
"#.to_string(),
        parameters: vec![
            TemplateParam {
                name: "WIDTH".to_string(),
                param_type: "integer".to_string(),
                default_value: "8".to_string(),
                description: "Data width in bits".to_string(),
            },
            TemplateParam {
                name: "DEPTH".to_string(),
                param_type: "integer".to_string(),
                default_value: "256".to_string(),
                description: "FIFO depth (words)".to_string(),
            },
        ],
    }
}

/// Generate FSM template
pub fn template_fsm() -> SourceTemplate {
    SourceTemplate {
        id: "fsm".to_string(),
        name: "Finite State Machine".to_string(),
        description: "Simple 3-state FSM example".to_string(),
        language: "verilog".to_string(),
        category: "Control".to_string(),
        source_code: r#"// Finite State Machine
// Generated by CovertEDA

module fsm (
    input wire clk,
    input wire rst_n,
    input wire trigger,
    output reg state_out
);

    localparam IDLE = 2'b00;
    localparam ACTIVE = 2'b01;
    localparam DONE = 2'b10;

    reg [1:0] state, next_state;

    always @(posedge clk) begin
        if (!rst_n) begin
            state <= IDLE;
        end else begin
            state <= next_state;
        end
    end

    always @(*) begin
        case (state)
            IDLE: next_state = trigger ? ACTIVE : IDLE;
            ACTIVE: next_state = DONE;
            DONE: next_state = IDLE;
            default: next_state = IDLE;
        endcase
    end

    assign state_out = state;

endmodule
"#.to_string(),
        parameters: vec![],
    }
}

/// Generate UART TX template
pub fn template_uart_tx() -> SourceTemplate {
    SourceTemplate {
        id: "uart_tx".to_string(),
        name: "UART Transmitter".to_string(),
        description: "UART transmitter with configurable baud rate".to_string(),
        language: "verilog".to_string(),
        category: "Interface".to_string(),
        source_code: r#"// UART Transmitter
// Generated by CovertEDA

module uart_tx #(
    parameter BAUD_RATE = 115200,
    parameter CLK_FREQ = 50_000_000
) (
    input wire clk,
    input wire rst_n,
    input wire [7:0] tx_data,
    input wire tx_valid,
    output wire tx_ready,
    output reg tx
);

    localparam BAUD_COUNT = CLK_FREQ / BAUD_RATE;

    reg [9:0] shift_reg;
    reg [13:0] baud_counter;
    reg [3:0] bit_count;
    reg transmitting;

    assign tx_ready = !transmitting;

    always @(posedge clk) begin
        if (!rst_n) begin
            tx <= 1'b1;
            transmitting <= 1'b0;
            baud_counter <= 0;
            bit_count <= 0;
        end else if (!transmitting && tx_valid) begin
            shift_reg <= {1'b1, tx_data, 1'b0};
            transmitting <= 1'b1;
            bit_count <= 0;
            baud_counter <= 0;
        end else if (transmitting) begin
            if (baud_counter == BAUD_COUNT - 1) begin
                tx <= shift_reg[0];
                shift_reg <= shift_reg >> 1;
                baud_counter <= 0;
                bit_count <= bit_count + 1'b1;
                if (bit_count == 9) transmitting <= 1'b0;
            end else begin
                baud_counter <= baud_counter + 1'b1;
            end
        end
    end

endmodule
"#.to_string(),
        parameters: vec![
            TemplateParam {
                name: "BAUD_RATE".to_string(),
                param_type: "integer".to_string(),
                default_value: "115200".to_string(),
                description: "UART baud rate".to_string(),
            },
            TemplateParam {
                name: "CLK_FREQ".to_string(),
                param_type: "integer".to_string(),
                default_value: "50000000".to_string(),
                description: "Clock frequency in Hz".to_string(),
            },
        ],
    }
}

/// Generate SPI Master template
pub fn template_spi_master() -> SourceTemplate {
    SourceTemplate {
        id: "spi_master".to_string(),
        name: "SPI Master".to_string(),
        description: "SPI Master controller".to_string(),
        language: "verilog".to_string(),
        category: "Interface".to_string(),
        source_code: r#"// SPI Master Controller
// Generated by CovertEDA

module spi_master #(
    parameter DATA_WIDTH = 8,
    parameter SPI_MODE = 0
) (
    input wire clk,
    input wire rst_n,
    input wire [DATA_WIDTH-1:0] mosi_data,
    output reg [DATA_WIDTH-1:0] miso_data,
    input wire start,
    output reg busy,
    output reg sclk,
    output reg mosi,
    input wire miso,
    output reg cs_n
);

    reg [3:0] bit_count;
    reg [DATA_WIDTH-1:0] tx_shift, rx_shift;
    reg [1:0] clk_div;

    always @(posedge clk) begin
        if (!rst_n) begin
            busy <= 1'b0;
            cs_n <= 1'b1;
            sclk <= (SPI_MODE[0] ? 1'b1 : 1'b0);
            bit_count <= 0;
        end else if (!busy && start) begin
            busy <= 1'b1;
            cs_n <= 1'b0;
            tx_shift <= mosi_data;
            bit_count <= 0;
        end else if (busy) begin
            if (clk_div == 2'd3) begin
                sclk <= ~sclk;
                if (!sclk) begin
                    mosi <= tx_shift[DATA_WIDTH-1];
                    tx_shift <= {tx_shift[DATA_WIDTH-2:0], 1'b0};
                end else begin
                    rx_shift <= {rx_shift[DATA_WIDTH-2:0], miso};
                    bit_count <= bit_count + 1'b1;
                    if (bit_count == DATA_WIDTH - 1) begin
                        busy <= 1'b0;
                        cs_n <= 1'b1;
                        miso_data <= rx_shift;
                    end
                end
                clk_div <= 0;
            end else begin
                clk_div <= clk_div + 1'b1;
            end
        end
    end

endmodule
"#.to_string(),
        parameters: vec![
            TemplateParam {
                name: "DATA_WIDTH".to_string(),
                param_type: "integer".to_string(),
                default_value: "8".to_string(),
                description: "SPI data width".to_string(),
            },
            TemplateParam {
                name: "SPI_MODE".to_string(),
                param_type: "integer".to_string(),
                default_value: "0".to_string(),
                description: "SPI mode (0-3)".to_string(),
            },
        ],
    }
}

